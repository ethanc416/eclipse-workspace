
public class Loops {

	public static void main(String[] args) {
		// Make a table using loops.
		
		int i = 1;
	    while( i < 6 )
	    {
	        System.out.println( i );
	        i++ ;
	        
	    }
	    
	    int x = 2;
	    while( x < 7 )
	    {
	        System.out.println( x );
	        x++ ;
	        
	    }

	}

}
